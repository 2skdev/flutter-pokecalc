  String get infoString {
    String str = '';

    if (power != null) {
      str += '威力:$power ';
    }
    if (accuracy != null) {
      if (accuracy == kAccuracyMax) {
        str += '命中:∞ ';
      } else {
        str += '命中:$accuracy ';
      }
    }

    str += 'PP:$pp ';

    return str;
  }

